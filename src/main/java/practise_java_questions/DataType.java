package practise_java_questions;

public class DataType {
    public static void main(String[] args) {

//        Data types:
//        primitive data types:
//        Integer family: byte, short, long, int.
//        Floating family: double, float.
//        character: char
//        boolean: boolean - true/ false.

//        1. byte:
//        Size: 8 bits = 1 byte
//        Range: -128 to 127
        byte b = 20;
        b = 120;

//        2. short:
//        Size: 16 bits = 2 bytes
//        Range: -32768 to 32767
        short s = 10;
        s = 30800;
//        10 vars = 10 bytes / 20 bytes

//        3. int:
//        Size: 32 bits = 4 bytes
//        Range: -2^31 to 2^32-1
        int k = 1000;
        k = 1005;

//        4. long:
//        Size: 64 bits = 8 byte
//        Range: -2^63 to 2^63-1
        long z = 98878889;
        z = 1209890;

//        5. float:
//        Size: 32 bits = 4 byte
//        Range: After point it can take up to 7 digits.
        float f = 98.98f;
        float t = (float) 17.970;

//        6. double:
//        Size: 64 bits = 8 byte
//        Range: After point it can take up to 15 digits.
        double u = 98.98;
        double u1 = 17.970;

//        7. char: only single digit value
//        Size: 16 bits: 2 bytes
        char c = 'f';
        char f1 = 'w';
        char c2 = '$';

//        8. boolean:
//        Size: ~ 1 bit.
        boolean flag = true;





    }
}
